using System;

namespace MyMath
{
    /// <summary>
    /// Operations class with mathematical methods.
    /// </summary>
    public class Operations
    {
        /// <summary>
        /// Adds two integers.
        /// </summary>
        /// <param name="a">First integer.</param>
        /// <param name="b">Second integer.</param>
        /// <returns>The sum of a and b.</returns>
        public static int Add(int a, int b)
        {
            return a + b;
        }
    }
}
